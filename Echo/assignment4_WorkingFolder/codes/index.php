<h3>Exam</h3>
<p>There will be an exam in February (exact date to be announced). You will need 60 % of all points received during the Exercises (being held in the tutorial on Fridays) in order to be allowed to participate in the Exam</p>
<h3>Last year students</h3>
<p>If you failed last years final exam you are allowed to retake it without the need of qualifying by receiving&nbsp;60% of the points from the homework assignments. However we strongly suggest you work through the exercises and also take part in the lecture and tutorial as some of the content might have changed in comparison to last year and&nbsp;reviewing the materials on a weekly basis seems also to be usefull.&nbsp;</p>
<h3>
Assignments</h3>
<p>For the assignments, you need to make a team of 3-4people. &nbsp;You can make your teams here as suggested by many students:&nbsp;<br />
